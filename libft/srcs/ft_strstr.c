#include "libft.h"

char	*ft_strstr(const char *big, const char *little)
{
	int		i;
	int		j;

	if (*little == '\0')
		return ((char*)big);
	i = 0;
	while (big[i] != '\0')
	{
		j = 0;
		while (little[j] == big[i + j])
		{
			if (little[j + 1] == '\0')
			{
				return (char*)(big + i);
			}
			j++;
		}
		i++;
	}
	return (NULL);
}
